# Lambda Expression
# to do the task the in one line.

def double_my_salary(salary):
    return salary * 2


new_salary = double_my_salary(100)
print(new_salary)


#By Lambda Expression
f_double_salary = lambda salary: salary * 2
print(f_double_salary(100))

